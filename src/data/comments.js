export const getCommentsByRestaurantId = (restaurantId) => {
    return comments
        .filter(comment => comment.restaurantId === restaurantId)
        .sort((a, b) => new Date(b.date) - new Date(a.date)); // Tri par date décroissante
};

export const getAverageRating = (restaurantId) => {
    const restaurantComments = comments.filter(comment => comment.restaurantId === restaurantId);
    if (restaurantComments.length === 0) return 0;
    
    const totalRating = restaurantComments.reduce((sum, comment) => sum + comment.rating, 0);
    return (totalRating / restaurantComments.length).toFixed(1);
};

export const getTotalCommentsCount = (restaurantId) => {
    return comments.filter(comment => comment.restaurantId === restaurantId).length;
};